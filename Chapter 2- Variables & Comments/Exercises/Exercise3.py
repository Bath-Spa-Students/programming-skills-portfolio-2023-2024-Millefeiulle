name = "\tJim Harrison\n"

# print the name so that whitespace gets displayed.
print("Unmodified:")
print(name)

# print the name using the function lstrip()
print("\nUsing lstrip():")
print(name.lstrip())

# print the name using the function rstrip()
print("\nUsing rstrip():")
print(name.rstrip())

# print the name using the function strip()
print("\nUsing strip():")
print(name.strip())